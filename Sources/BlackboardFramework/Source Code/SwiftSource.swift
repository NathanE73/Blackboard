import Foundation

class SwiftSource {
    
    private var lines: [(indentLevel: Int, line: String)] = []
    
    func append(_ line: String = "", indentLevel: Int? = nil) {
        let indentLevel = indentLevel ?? self.indentLevel
        
        if line.isEmpty && line == lines.last?.line {
            return // don't allow multiple blank lines
        }
        
        if insideCommentBlock {
            line.components(separatedBy: CharacterSet.newlines)
                .forEach { comment in
                    lines.append((indentLevel, "/// \(comment)"))
                }
        } else {
            lines.append((indentLevel, line))
        }
    }
    
    func append(_ line: String, block: () -> Void) {
        append("\(line) {")
        
        let initialCount = lines.count
        
        indent(block)
        
        if lines.count - initialCount == 1, lines.last?.line.isEmpty == true {
            lines.removeLast()
        }
        
        append("}")
    }
    
    var source: String {
        var source: [String] = []
        
        var indentLevel = 0
        var indent = ""
        
        lines.forEach { entry in
            if indentLevel != entry.indentLevel {
                indentLevel = entry.indentLevel
                indent = ""
                for _ in 0 ..< (indentLevel) {
                    indent.append("    ")
                }
            }
            
            source.append("\(indent)\(entry.line)")
        }
        
        return source.joined(separator: "\n")
    }
    
    // MARK: Indent Level
    
    var indentLevel = 0
    
    func indent() {
        indentLevel += 1
    }
    
    func unindent() {
        indentLevel = max(0, indentLevel - 1)
    }
    
    func indent(_ block: (() -> Void)) {
        indent()
        block()
        unindent()
    }
    
    // MARK: Comment Block
    
    var insideCommentBlock = false
    
}

extension SwiftSource {
    
    func append(source: String) -> Self {
        source.components(separatedBy: CharacterSet.newlines)
            .forEach { append($0) }
        return self
    }
    
    func commentBlock(block: () -> Void) {
        insideCommentBlock = true
        
        block()
        
        insideCommentBlock = false
    }
    
    func directive(_ line: String = "") {
        append(line, indentLevel: 0)
    }
    
    func appendHeading(filename: String, modules: [String], includeBundle: Bool = false) {
        append("//")
        append("//  \(filename)")
        append("//")
        append("//  This file is automatically generated; do not modify.")
        append("//")
        append()
        modules.forEach { module in
            append("import \(module)")
        }
        append()
        
        if includeBundle {
            append("private let bundle: Bundle = {")
            directive("#if SWIFT_PACKAGE")
            indent {
                append("Bundle.module")
            }
            directive("#else")
            indent {
                append("class Object: NSObject { }")
                append("return Bundle(for: Object.self)")
            }
            directive("#endif")
            append("}()")
            append()
        }
    }
    
    func appendAssetItems<Asset>(_ assetItems: [AssetItem<Asset>], block: (_ asset: Asset) -> Void) {
        assetItems.forEach { asset in
            switch asset {
            case let .asset(asset):
                block(asset)
            case let .namespace(namespace, assets):
                let namespace = Naming.lastNamespaceName(from: namespace)
                append("enum \(namespace)") {
                    appendAssetItems(assets, block: block)
                }
            }
        }
    }
    
}
